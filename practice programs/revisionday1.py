# Two types of loop
# 1. For loop
# 2.While loop

# Print Even numbers uptill 20 with both loops

# For loop

print("Even numbers with the For loop")

for i in range(0,22,1):   # range(start,end,step)
    if(i%2!=0):
        print(i)

# While Loop

# while(condition):
#     statements..
#     increments...


print("Even numbers with the while loop")
n=0
while(n<=20):
    if(n%2!=0):
        print(n)
    n=n+1





